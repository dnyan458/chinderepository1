// prime number
package New;
public class primenumber{
public static void main(String args[]){
int num=8;
int count=0;
for(int i=0;i<=num;i++)
{

if(num!=8 )
{
count++;
}
}
if(count<=8)
{
System.out.println("given number is prime number");
}

else
{System.out.println("Given number is not a prime number");
}
}
}