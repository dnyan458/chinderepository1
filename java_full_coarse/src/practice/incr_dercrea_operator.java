package practice;

public class incr_dercrea_operator
{

	public static void main(String[] args)
	{
		int a=10;


        System.out.println(a--);//10--->9
        System.out.println(--a);//8
        System.out.println(a++);//8--->9
        System.out.println(++a);//10


		a+=10;
        System.out.println(a);


	}

}
